#Довольно простая команда. Показывает свободное пространство
#на диске (disk free)

#===================================
pkill
pkill (или process kill) 
# завершает запущенный процесс. 
# Эта команда особенно полезна, когда приложение не отвечает:
pkill [application_name]
# Веселой/жестокой команда может оказаться, если вы
# запускаете ее на удаленной машине. Будьте осторожны, 
# можно потерять важные данные.

