#===================================
[space] [command]
#Опытные пользователи, возможно, знают, что история 
#запущенных команд сохраняется в файле ~/.bash_history.
#Так, чтобы команда не записывалась в историю, 
#просто наберите перед командой пробел.

#===================================
jot
#jot генерирует текст. Числа, текст, все вместе. 
#Чтобы сгенерировать числа, попробуйте следующее:
jot [количество чисел] [начиная с]
#Еiterсли пропишете один аргумент, сгенерируются числа от 1
#до значения аргумента.

#Чтобы получить случайные:
jot -r [количество чисел] [нижний предел] [верхний предел]


#===================================
