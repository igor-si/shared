
# Syntax: 
print(value(s), sep= ' ', end = '\n', file=file, flush=flush)

# Parameters:
# value(s) : Any value, and as many as you like.
# Will be converted to string before printed
# sep=’separator’ : (Optional) Specify how 
# to separate the objects, if there is more than one.Default :’ ‘
# end=’end’: (Optional) Specify what to print at the end.Default : ‘\n’
# file : (Optional) An object with a write method. Default :sys.stdout
# flush : (Optional) A Boolean, specifying 
# if the output is flushed (True) or buffered (False). Default: False

# Returns: It returns output to the screen.

